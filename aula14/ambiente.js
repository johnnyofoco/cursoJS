//While
var c = 1

console.log('--------------------')
console.log('While:')
while (c <= 5) {
    console.log(c)
    c++
}
//Do While
console.log('--------------------')
console.log('Do While:')

c = 1
do {
    console.log(c)
    c++
} while (c <= 5)

//For
console.log('--------------------')
console.log('For:')

for (var c = 1; c <= 5; c++) {
    console.log(c)
}
console.log('Fim!')
//While
var c = 1

while (c <= 3) {
    console.log(`Tudo bem ? : ${c}`)
    c++ // c = c + 1
}

//Do While
c = 1

do {
    console.log(`E ae? : ${c}`)
    c++ // c = c + 1
} while (c <= 3)


//For
for (let c = 1; c < 11; c++) {
    console.log(`${c}`);

}
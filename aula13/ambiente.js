//While
var c = 1

while (c <= 3) {
    console.log(`Tudo bem ? : ${c}`)
    c++ // c = c + 1
}

//Do While
c = 1

do {
    console.log(`E ae? : ${c}`)
    c++ // c = c + 1
} while (c <= 3)


//For

for (let index = 1; index < 11; index++) {
    console.log(`${index}`);

}